
const tree = (state=[], action) =>{
    switch(action.type){
        case 'ADD_FILE':
            return  "";
        case 'ADD_FOLDER':
            return "";
        case 'DELETE_ITEM':
            return "";
        case 'RENAME_ITEM':
            return "";
        case 'TOGGLE_FOLDER':
            return "";
        default:
            return state
    }

}

export default tree